/*******************************************************************************
* Function Name  : USART1_IRQHandler
* Description    : This function handles USART1 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART1_IRQHandler(void)
{
	u8 receive;
	if(USART_GetITStatus(USART1, USART_IT_RXNE) == SET){
		USART_ClearITPendingBit(USART1, USART_IT_RXNE);
	  	receive = USART_ReceiveData(USART1);    
		//启动定时器6
		ModbusTimersEnable();
		if(jieshouweizhi_485 < MODBUS_JIESHOU_LEN){
			jieshou_485[jieshouweizhi_485++] = receive;
		}
		else{
			
		}
	}
	else if(USART_GetITStatus(USART1, USART_IT_TXE) == SET){
		USART_ClearITPendingBit(USART1, USART_IT_TXE);
		if(fasongweizhi_485 < MODBUS_FASONG_LEN){
			USART_SendData(USART1, fasong_485[fasongweizhi_485++]);
		}
		else{
			USART_ITConfig(USART1, USART_IT_TXE,DISABLE);
			fasongweizhi_485=0;
			Delay_ms(2);
			MODBUS_READ_DATA();
			//发送完成切换成输入状态
		}
	}
	//溢出-如果发生溢出需要先读SR,再读DR寄存器 则可清除不断入中断的问题
	if(USART_GetFlagStatus(USART1,USART_FLAG_ORE)==SET)
	{
		USART_ClearFlag(USART1,USART_FLAG_ORE);    //读SR
		USART_ReceiveData(USART1);                //读DR
	}
}

